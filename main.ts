let number = 0
while (!(input.isGesture(Gesture.Shake))) {
    basic.showString("I")
    basic.pause(500)
    basic.showIcon(IconNames.Heart)
    basic.pause(500)
    basic.showString("U")
    basic.pause(500)
}
